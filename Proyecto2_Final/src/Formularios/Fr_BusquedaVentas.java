    public void llenartabla2() {
        Rel_productosventasDAO rpr = control.getRel_productosventasrepository();
        ArrayList<Rel_productosventas> rplist = rpr.consultar();
        ArrayList<Rel_productosventas> almacenador = new ArrayList<>();
        for (Rel_productosventas prod_vent : rplist) {
            if (prod_vent.getVenta().getIdventa() == Long.valueOf(txtId.getText())) {
                almacenador.add(prod_vent);
            }
        }

        DefaultTableModel modelo = (DefaultTableModel) tblProductos.getModel();
        modelo.setRowCount(0);
        for (Rel_productosventas tablita : almacenador) {
            Object[] fila = new Object[5];
            fila[0] = tablita.getProducto().getIdproducto();
            fila[1] = tablita.getVenta().getCliente().getNombre();
            fila[2] = tablita.getPrecio();

            fila[3] = tablita.getCantidad();
            fila[4] = tablita.getMontototal();

            modelo.addRow(fila);
        }

    }

