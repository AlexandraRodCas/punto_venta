    public void llenarcampos() {
        int indiceFila = tblBuscarVentas.getSelectedRow();
        VentasDAO vr = control.getVentasRepository();
        Venta venta = vr.buscarPorId((long) tblBuscarVentas.getValueAt(indiceFila, 0));
        this.txtId.setText(venta.getIdventa() + "");
        this.txtCliente.setText(venta.getCliente().getNombre());
        this.txtDescuento.setText(venta.getDescuento() + "");
        this.txtTotal.setText(venta.getMontofinal() + "");
        float subtotal = Float.valueOf(txtTotal.getText());
        float estacosa = Float.valueOf(txtDescuento.getText());
        float finalj = subtotal + estacosa;
        this.txtSubtotal.setText(finalj + "");
    }
