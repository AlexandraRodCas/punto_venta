    public void llenarDatespickers() {
        Calendar calendar = Calendar.getInstance();
        Date date = new Date(calendar.get(Calendar.YEAR) - 1900, calendar.get(Calendar.MONTH), calendar.get(Calendar.DAY_OF_MONTH));
        Desde.setDate(date);
        Hasta.setDate(date);
    }
    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed

        ArrayList<Venta> totalventas = control.getVentasRepository().consultar();
        ArrayList<Venta> ventasGuardadas = new ArrayList<>();

        Calendar desdecalendar = dateToCalendar(Desde.getDate());

        Calendar hastacaCalendar = dateToCalendar(Hasta.getDate());
        for (Venta ventasEspecificada : totalventas) {
            
            if (Enmedio(desdecalendar, hastacaCalendar, ventasEspecificada.getFecha()) && ventasEspecificada.getCliente().equals(cB_Clientes.getSelectedItem())) {
                ventasGuardadas.add(ventasEspecificada);
            }

        }
        DefaultTableModel modelo = (DefaultTableModel) tblBuscarVentas.getModel();
        modelo.setRowCount(0);
        for (Venta prov : ventasGuardadas) {
            Object[] fila = new Object[6];
            fila[0] = prov.getIdventa();
            fila[1] = prov.getCliente().getIdcliente();
            Calendar fecha = prov.getFecha();
            System.out.println(prov.getFecha().toString());

            fila[2] = fecha.get(Calendar.YEAR) + "/" + (fecha.get(Calendar.MONTH)+1) + "/"
                    + (fecha.get(Calendar.DAY_OF_MONTH)+1);

            float montoTotal = prov.getMontofinal();
            float descuento = prov.getDescuento();

            float subtotal = montoTotal + descuento;

            fila[3] = subtotal;
            fila[4] = descuento;
            fila[5] = montoTotal;

            modelo.addRow(fila);
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed
    public boolean Enmedio(Calendar desde, Calendar hasta, Calendar fechaBuscar) {
        
        if (fechaBuscar.compareTo(desde) >= 0 && fechaBuscar.compareTo(hasta) <= 0) {
            return true;
        }
        return false;

    }

    private Calendar dateToCalendar(Date date) {

        Calendar calendar = Calendar.getInstance();
        calendar.setTime(date);
        return calendar;

    }
