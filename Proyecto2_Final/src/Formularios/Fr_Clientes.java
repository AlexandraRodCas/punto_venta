    public void buscar() {
        if (!txtBuscar.getText().equals("") && ValidarSoloNumeros(txtBuscar.getText())) {

            Cliente clienteBuscado = clientesRepository.buscarPorId(Long.valueOf(txtBuscar.getText()));
            DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();
            modelo.setRowCount(0);
            if (clienteBuscado != null) {
                Object[] fila = new Object[6];
                fila[0] = clienteBuscado.getIdcliente();
                fila[1] = clienteBuscado.getNombre();
                fila[2] = clienteBuscado.getRfc();
                fila[3] = clienteBuscado.getDireccion();
                fila[4] = clienteBuscado.getTelefono1();
                fila[5] = clienteBuscado.getTelefono2();

                modelo.addRow(fila);
            } else {
                JOptionPane.showMessageDialog(this, "No existe cliente con este Id", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Introdusca solo numeros porfavor", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        vaciarCampos();
    }

    public void buscarporNombre(String Nombre) {
        if (!txtBuscar.getText().equals("") && ValidarSoloLetras(txtBuscar.getText())) {
            ArrayList<Cliente> clientes = clientesRepository.consultar();
            DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();
            Cliente cliente = null;

            for (Cliente cliente1 : clientes) {
                
                if (cliente1.getNombre().equalsIgnoreCase(Nombre)) {
                    cliente = cliente1;
                }

                if (cliente != null) {
                    modelo.setRowCount(0);
                    Object[] fila = new Object[6];
                    fila[0] = cliente.getIdcliente() + "";
                    fila[1] = cliente.getNombre();
                    fila[2] = cliente.getRfc();
                    fila[3] = cliente.getDireccion();
                    fila[4] = cliente.getTelefono1();
                    fila[5] = cliente.getTelefono2();

                    modelo.addRow(fila);

                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Introduzca un valor valido", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        vaciarCampos();
    }
