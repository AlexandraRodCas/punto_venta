    public void eliminar() {
        int indiceFila = tblClientes.getSelectedRow();
        if (indiceFila == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un cliente", "Información", JOptionPane.ERROR_MESSAGE);
        } else {
            long idcliente = (Long) tblClientes.getValueAt(indiceFila, 0);
            clientesRepository.Eliminar(idcliente);
            llenartabla();
        }
    }

